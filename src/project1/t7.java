public class t7 {
    public static void main(String[] args) {
        //Write a Java Program to print the first 10 numbers of Fibonacci series.


        int[] numbers = {0, 1, 1, 2, 3, 5, 5, 56, 67, 65, 78, 87, 678, 435, 322};
        for (int i = 0; i < 10; i++) {
            System.out.print(numbers[i] + " ");
        }
        System.out.println();
    }
}
